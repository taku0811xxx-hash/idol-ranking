export function generateAppeal(idol: any) {
  const name = idol.name;
  const genre = idol.genre || "タレント";
  const bio = idol.bio || "";
  const tags = idol.tags || [];


  let text = "";

  // ======================
  // bioベース分岐（強化版）
  // ======================

  if (bio.includes("コスプレ")) {
    text = `${name}は${bio}として知られ、日本国内外で高い人気を誇る存在である。
    キャラクターの再現度の高さや表現力に定評があり、
    SNSやイベントを通じて多くのファンを魅了している。
    また、グラビアやメディア出演など活動の幅も広げている。`;
  }

  else if (bio.includes("インフルエンサー")) {
    text = `${name}は${bio}としてSNSを中心に注目を集めている存在であり、
    フォロワー数の増加とともに人気が急上昇している。
    投稿される写真や動画は高い支持を集めており、
    グラビアやメディア出演など活動の幅も広がっている。`;
  }

  else if (bio.includes("ミスマガ")) {
    text = `${name}は${bio}として注目を集め、
    ミスマガジン受賞をきっかけに一気に知名度を上げた存在である。
    その後はグラビアやメディア出演など幅広く活躍し、
    若手の中でも特に人気の高い存在として知られている。`;
  }

  else if (bio.includes("アイドル")) {
    text = `${name}は${bio}として活動し、
    その可愛らしいルックスとパフォーマンスで多くのファンを獲得している。
    アイドル活動に加え、グラビアやテレビ出演など活躍の場を広げており、
    今後の成長にも期待が集まっている。`;
  }

  else if (bio.includes("女優")) {
    text = `${name}は${bio}として活躍する人物であり、
    映画やドラマ、CMなど多方面でその存在感を発揮している。
    演技力とビジュアルの両面で評価されており、
    若手から実力派まで幅広い層のファンを持つ。`;
  }

  else if (bio.includes("レースクイーン")) {
    text = `${name}は${bio}として活動し、
    その抜群のスタイルとビジュアルで注目を集めている。
    レースクイーンとしての活動をきっかけに人気を獲得し、
    現在はグラビアやモデルなど多方面で活躍している。`;
  }

  else {
    text = `${name}は${genre}として活動し、
    多くのファンから支持を集めている人物である。
    ビジュアルやキャラクター性の高さから注目されており、
    今後の活躍にも期待が集まっている。`;
  }

  // タグ
  if (tags.length > 0) {
    text += ` ${tags.join("・")}といった特徴でも人気が高い。`;

    if (idol.catch) {
  text += ` 「${idol.catch}」としても知られている。`;
}
  }

  return text;
  
}